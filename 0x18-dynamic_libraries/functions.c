#include <stdio.h>
/**
 * add - Function that adds two parameters
 * @a: 1st parameter
 * @b: 2nd parameter
 * Return: The Sum of parameters
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Function that subtracts two parameters
 * @a: 1st parameter
 * @b: 2nd parameter
 * Return: The Subtraction of parameters
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Function that multiplies two parameters
 * @a: 1st parameter
 * @b: 2nd parameter
 * Return: The Multiplication of parameters
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Function that divides two parameters
 * @a: 1st parameter
 * @b: 2nd parameter
 * Return: The Division of parameters
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Function that find the mod for two parameters
 * @a: 1st parameter
 * @b: 2nd parameter
 * Return: The Mod of parameters
 */
int mod(int a, int b)
{
	return (a % b);
}
